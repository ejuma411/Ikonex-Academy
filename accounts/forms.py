class TeacherAssignmentForm(forms.Form):
    """Form for assigning class teacher (Issues #3 & #8)."""
    teacher = forms.ModelChoiceField(
        queryset=User.objects.filter(role='teacher'),
        widget=forms.Select(attrs={'class': 'form-control'}),
        label="Teacher"
    )
    class_stream = forms.ModelChoiceField(
        queryset=ClassStream.objects.all(),
        widget=forms.Select(attrs={'class': 'form-control'}),
        label="Class Stream"
    )
    academic_year = forms.CharField(
        max_length=9,
        widget=forms.TextInput(attrs={'class': 'form-control', 'placeholder': 'e.g., 2026/2027'}),
        label="Academic Year"
    )

    def clean_academic_year(self):
        """Validate academic year format and ensure it's current or future (Issue #3)."""
        academic_year = self.cleaned_data.get('academic_year', '').strip()

        if not re.match(r'^\d{4}/\d{4}$', academic_year):
            raise forms.ValidationError("Use the format YYYY/YYYY+1, e.g., 2026/2027.")

        start_str, end_str = academic_year.split('/')
        start_year, end_year = int(start_str), int(end_str)

        if end_year != start_year + 1:
            raise forms.ValidationError("Academic year must span consecutive years, e.g., 2026/2027.")

        current_year = date.today().year
        if start_year < current_year:
            raise forms.ValidationError(
                f"Academic year cannot be in the past. Use {current_year}/{current_year + 1} or later."
            )

        return academic_year

    def clean(self):
        """Prevent a teacher from being assigned to more than one class per year (Issue #8)."""
        cleaned_data = super().clean()
        teacher = cleaned_data.get('teacher')
        academic_year = cleaned_data.get('academic_year')

        if teacher and academic_year:
            existing = ClassTeacherAssignment.objects.filter(
                teacher=teacher,
                academic_year=academic_year,
                is_active=True,
            )
            if existing.exists():
                assigned_class = existing.first().class_stream.name
                raise forms.ValidationError(
                    f"{teacher.get_full_name() or teacher.username} is already the class teacher for "
                    f"'{assigned_class}' in {academic_year}. Each teacher can only hold ONE class per year."
                )

        return cleaned_data


